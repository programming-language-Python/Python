for i in range(1, 5):
    print(i)
else:
    print('Цикл for закончен')
''' блок else не обязателен. 
Если он присутствует, он всегда
 выполняется один раз после окончания
  цикла for, если только не указан
   оператор break.
   
   
Если в C/C++ записать for
 (int i = 0; i < 5; i++),то в Python 
 этому соответствовало бы выражение 
 for i in range(0,5).Как видно, в 
 Python цикл for проще, более выразителен 
 и менее подвержен ошибкам.
'''